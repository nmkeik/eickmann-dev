    <h1>Historie</h1>
    <section>

        <p>
            Begonnen hat Eickmann Computer, gegründet von Ulrich Eickmann in einem kleinen Ladengeschäft im Jahr 1986. Schon bald danach kam die erste Erweiterung mit einem weiteren Ladengeschäft. Der bisherige Laden wurde in Werkstatt und Administration umgewandelt, im zweiten Ladengeschäft waren der Service und Verkauf untergebracht. Aber auch diese Konstellation erwies sich nach nicht allzu langer Zeit als zu eng. Denn mittlerweile hatte sich Eickmann Computer zu einem Unternehmen mit 12 festen Mitarbeiter/innen erweitert. Die nächste Erweiterung bestand aus ...
        </p>

        <h2>Eickmann Business und DTP Center</h2>
        <p>
            Werkstatt, Administration, Versand, Lager und ein neuer Präsentationsbereich. Hier wurden Präsentationen zu fast allen Bereichen durchgeführt, die mit der Hardware von APPLE, ATARI, ACORN, NEXT und MS DOS Kompatiblen, erreichbar waren.
        </p>

        <h2>Das Eickmann Schulungszentrum</h2>
        <p>
            Die alte Werkstatt wurde zu einem Schulungszentrum umgestaltet. Dort wurden Software und Schulungen aller Systeme, durchgeführt. Durch Kooperation mit Schulungsfirmen wurde dieser Dienstleistungsbereich bei Eickmann Computer stark erweitert. 
        </p>

        <h2>Der Eickmann Computer Shop</h2>
        <p>
            Dort befand sich eine große Auswahl an Computern, Software, Zubehör, Literatur und Verbrauchsartikeln. Auch ausgefallenere Wünsche konnten erfüllt werden. Ein freundliches Fachpersonal half stets gerne weiter.
            Den Kunden wurden nur Lösungen angeboten mit denen die tägliche Arbeit (oder das Hobby) effektiver gestaltet werden konnten. Hierzu gehörte selbstverständlich auch ein Rundum Service, der Technikabteilung, welche eine Reparatur oder Erweiterung binnen 3 Werktagen durchgeführt hat. Auch ein vor Ort Service war zu dieser Zeit bereits möglich, sofern dieses technisch möglich war.
        </p>

        <p>
            Nachdem der zwar technisch hochentwickelte aber leider zu wenig verbreitete Atari immer mehr durch den klassischen PC ersetzt wurde, musste auch Eickmann Computer sich am Markt neu orientieren.
        </p>

        <p>
            Nach ein paar Jahren der Umstellung und Konsolidierung, hat Eickmann Computer neue Schwerpunkte im Angebot gesetzt. 
        </p>

        <p>
            Mittlerweile bietet Eickmann Computer über 15 Jahre an Kompetenz, Erfahrung und legt dabei noch immer besonderen Wert auf individuelle Beratung und persönlichen Service.
        </p>

        <p>
            Alles aus einer Hand in den Bereichen EDV und Telekommunikation ist jetzt das Motto, freundlich, hilfsbereit und immer auf dem aktuellsten Stand der Technik ist das Team, individuell und auf Ihre persönlichen Wünsche abgestimmt ist das Angebot. Egal ob ein PC zur privaten Nutzung, zur reinen Textverarbeitung, zum Surfen im Internet oder für Spiele gesucht wird oder ob es um eine optimale Lösung eines Unternehmens in den Fragen EDV, Netzwerk, Internetanbindung und Telekommunikation geht. Auch die Gestaltung von Internetseiten, gerne auch barrierefrei und die Programmierung von Datenbanken stehen im Angebot. Von der sachkundigen Beratung, über die sorgfältige Planung und das Projektmanagement, der kompletten Installation und wenn gewünscht auch Lieferung vor Ort, der sorgfältigen Einweisung und dem zeitnahen Support bietet Eickmann Computer alles an. 
        </p>
    </section>

                    Unterschrift auf dem Lieferschein den Empfang der Lieferung. Ab diesem Zeitpunkt beginnt die
                    Gewährleistung.
                </li>
                <li>
                    Treten nach Übergabe der Ware an den Käufer Mängel in der Hardware oder Software auf, kann der
                    Käufer von Eickmann Computer die ordnungsgemäße Wiederherstellung verlangen. Die Gewährleistung
                    erfolgt in den Räumlichkeiten von Eickmann Computer. Der Käufer ist berechtigt bei zwei
                    fehlgeschlagenen Versuch der Wiederherstellung den Kaufpreis zu mindern.
                </li>

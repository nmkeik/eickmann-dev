        <p>
            Auf gesonderte Vereinbarung stellt Eickmann Computer die technische Betriebsbereitschaft der Geräte durch
            Installation an einem vom Kunden gewünschten Lieferort her. Die ordnungsgemäße Installation und die
            Betriebsbereitschaft der Geräte ist von dem Käufer schriftlich auf dem Lieferschein zu bestätigen.
        </p>


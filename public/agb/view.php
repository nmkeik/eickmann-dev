        <li class="header">Allgemein</li>
        <p>
            Der Käufer erwirbt von Eickmann Computer, Inhaber Ulrich Eickmann, die in der Rechnung bezeichneten Geräte,
            Geräteteile, Betriebssoftware und Dokumentation oder Serviceleistungen zu den folgenden Bedingungen. Die
            jeweiligen Rechnungen sind Bestandteil dieses Vertrages.
        </p>

        <li class="header">
            Erfüllungsort, Gefahrenübergang und Lieferung
            <ol>
                <li>
                    Vertraglicher Erfüllungsort für beide Vertragspartner sind die Geschäftsräume von Eickmann
                    Computer.
                </li>
                <li>
                    Mit der Übergabe der verkauften Waren geht die Gefahr des zufälligen Untergangs und der zufälligen
                    Verschlechterung auf den Käufer über.
                </li>
                <li>
                    Wird die Ware auf Wunsch des Käufers versendet, so steht die Versandart, der Versandweg und die mit
                    dem Versand beauftragte Firma im Ermessen von Eickmann Computer, sofern keine ausdrücklichen
                    Weisungen durch den Käufer erfolgt sind. Der Käufer hat in diesem Fall die Kosten des Versandes, der
                    Bearbeitung, sowie der Nachnahme zu übernehmen. Die Gefahr des zufälligen Unterganges oder der
                    zufälligen Verschlechterung geht auf den Käufer über, sobald Eickmann Computer die Ware dem
                    Spediteur, dem Frachtführer oder sonst zur Ausführung der Versendung bestimmten Personen übergeben
                    hat.
                </li>
                <li>
                    Verweigert der Käufer die Annahme der versendeten Ware, ist Eickmann Computer berechtigt auf Abnahme
                    zu bestehen oder 10 % des Kaufpreises als pauschalierter Schadens- und Aufwendungsersatz, jedoch
                    mindestens die Höhe der Auslagen für Porto und Versand, zu verlangen. Die pauschale Entschädigung
                    mindert sich in dem Maße, wie der Käufer nachweist, dass Aufwendungen oder der Schaden nicht
                    entstanden ist.
                </li>
                <li>
                    Eickmann Computer ist zu Teillieferungen berechtigt, soweit nicht ausdrücklich etwas anderes
                    vereinbart wurde. Teillieferungen gelten für Zahlungsverpflichtungen, Gefahrenübergang und
                    Gewährleistungspflichten als selbständige Lieferung. Der Käufer ist nicht berechtigt, selbständige
                    Teillieferungen zurückzuweisen.
                </li>
                <li>
                    Die Lieferzeit verlängert sich angemessen bei Maßnahmen im Rahmen von Arbeitsausfällen, insbesondere
                    Streik und Aussperrung sowie von Eickmann Computer nicht zu vertretenden Umstände, wie gesetzlicher
                    oder behördlicher Anordnung sowie in Fällen von Lieferverzögerungen aufgrund höherer Gewalt.
                </li>
                <li>
                    Gerät Eickmann Computer in Verzug mit der Lieferung, so bestehen Schadensersatzansprüche lediglich,
                    wenn der Verzug auf grober Fahrlässigkeit oder Vorsatz beruht. Dies gilt auch für Erfüllungsgehilfen
                    von Eickmann Computer.
                </li>
            </ol>
        </li>


        <li class="header">Allgemein</li>
        <p>
            Der Käufer erwirbt von Eickmann Computer, Inhaber Ulrich Eickmann, die in der Rechnung bezeichneten Geräte,
            Geräteteile, Betriebssoftware und Doku-mentation oder Serviceleistungen zu den folgenden Bedingungen. Die
            jeweili-gen Rechnungen sind Bestandteil dieses Vertrages.
